function edges = eelsEdgeTable()
%EELSEDGETABLE  Built-in table of common EELS core-loss ionization edges.
%
%   Syntax:
%       edges = imaging.eelsEdgeTable()
%
%   Returns a struct array of common EELS core-loss edges with onset energies
%   compiled from Egerton (2011) and the EELS Atlas.  No external toolboxes
%   are required; data are hard-coded.
%
%   Outputs:
%       edges — struct array, one entry per edge, with fields:
%                 .element  — element symbol ('C', 'Fe', etc.)
%                 .edge     — edge label ('K', 'L23', 'M45', etc.)
%                 .onsetEV  — threshold energy (eV)
%                 .symbol   — display string ('C-K', 'Fe-L23', etc.)
%                 .Z        — atomic number
%
%   Examples:
%       edges = imaging.eelsEdgeTable();
%       % Find all edges between 400 and 900 eV
%       mask = [edges.onsetEV] >= 400 & [edges.onsetEV] <= 900;
%       disp({edges(mask).symbol});
%
%       % Look up the Fe-L23 onset
%       idx = strcmp({edges.symbol}, 'Fe-L23');
%       fprintf('Fe-L23 onset: %d eV\n', edges(idx).onsetEV);
%
%   See also imaging.eelsBackground, imaging.eelsExtractMap

% ════════════════════════════════════════════════════════════════════════
%  Edge data  {element, edge, onsetEV, Z}
%  Sources: Egerton "Electron Energy-Loss Spectroscopy in the Electron
%           Microscope" (3rd ed., 2011); EELS Atlas (Ahn & Krivanek 1983).
% ════════════════════════════════════════════════════════════════════════
raw = { ...
    'Li',  'K',   55,   3;  ...
    'B',   'K',  188,   5;  ...
    'C',   'K',  284,   6;  ...
    'N',   'K',  401,   7;  ...
    'O',   'K',  532,   8;  ...
    'F',   'K',  685,   9;  ...
    'Na',  'K', 1072,  11;  ...
    'Mg',  'K', 1305,  12;  ...
    'Al',  'L23',  73,  13;  ...
    'Al',  'K',  1560,  13;  ...
    'Si',  'L23',  99,  14;  ...
    'Si',  'K',  1839,  14;  ...
    'P',   'L23', 135,  15;  ...
    'S',   'L23', 165,  16;  ...
    'Cl',  'L23', 200,  17;  ...
    'Ar',  'L23', 245,  18;  ...
    'K',   'L23', 294,  19;  ...
    'Ca',  'L23', 346,  20;  ...
    'Sc',  'L23', 397,  21;  ...
    'Ti',  'L23', 456,  22;  ...
    'V',   'L23', 513,  23;  ...
    'Cr',  'L23', 575,  24;  ...
    'Mn',  'L23', 640,  25;  ...
    'Fe',  'L23', 708,  26;  ...
    'Co',  'L23', 778,  27;  ...
    'Ni',  'L23', 855,  28;  ...
    'Cu',  'L23', 931,  29;  ...
    'Zn',  'L23',1020,  30;  ...
    'Ga',  'L23',1115,  31;  ...
    'Ge',  'L23',1217,  32;  ...
    'As',  'L23',1323,  33;  ...
    'Se',  'L23',1436,  34;  ...
    'Br',  'L23',1550,  35;  ...
    'Y',   'L23',2080,  39;  ...
    'Zr',  'L23',2222,  40;  ...
    'Nb',  'M45', 205,  41;  ...
    'Mo',  'M45', 227,  42;  ...
    'Ru',  'M45', 279,  44;  ...
    'Pd',  'M45', 335,  46;  ...
    'Ag',  'M45', 367,  47;  ...
    'Sn',  'M45', 485,  50;  ...
    'Sb',  'M45', 528,  51;  ...
    'Ba',  'M45', 781,  56;  ...
    'La',  'M45', 832,  57;  ...
    'Ce',  'M45', 883,  58;  ...
    'Sr',  'L23',1940,  38;  ...
    'W',   'M45',1809,  74;  ...
    'Pt',  'M45',2122,  78;  ...
    'Au',  'M45',2206,  79;  ...
};

% ════════════════════════════════════════════════════════════════════════
%  Build struct array
% ════════════════════════════════════════════════════════════════════════
nEdges = size(raw, 1);
edges  = struct('element', cell(nEdges,1), ...
                'edge',    cell(nEdges,1), ...
                'onsetEV', cell(nEdges,1), ...
                'symbol',  cell(nEdges,1), ...
                'Z',       cell(nEdges,1));

for k = 1:nEdges
    edges(k).element = raw{k,1};
    edges(k).edge    = raw{k,2};
    edges(k).onsetEV = raw{k,3};
    edges(k).Z       = raw{k,4};
    edges(k).symbol  = sprintf('%s-%s', raw{k,1}, raw{k,2});
end

end
